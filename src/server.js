    let isDragging = false;

    document.getElementById('bannerContainer').addEventListener('touchstart', touchStart, false);
    document.getElementById('bannerContainer').addEventListener('touchmove', touchMove, false);
    document.getElementById('bannerContainer').addEventListener('touchend', touchEnd, false);

    function touchStart(e) {
        const touch = e.touches[0];
        startX = touch.clientX;
        startY = touch.clientY;
        isDragging = true;
    }

    function touchMove(e) {
        if (!isDragging) return;
        const touch = e.touches[0];
        const deltaX = touch.clientX - startX;
        const deltaY = touch.clientY - startY;
        if (Math.abs(deltaX) > Math.abs(deltaY)) {
            e.preventDefault();
            dist = deltaX;
        }
    }

    function touchEnd() {
        if (!isDragging) return;
        isDragging = false;
        if (dist > 50) {
            prevSlide();
        } else if (dist < -50) {
            nextSlide();
        }
        dist = 0;
    }

    let slideIndex = 0;

    function showSlides() {
        const slides = document.querySelectorAll('.banner');
        if (slideIndex >= slides.length) {
            slideIndex = 0;
        } else if (slideIndex < 0) {
            slideIndex = slides.length - 1;
        }
        const translateX = -slideIndex * 100 + '%';
        document.getElementById('bannerSlider').style.transform = `translateX(${translateX})`;
    }

    function nextSlide() {
        slideIndex++;
        showSlides();
    }

    function prevSlide() {
        slideIndex--;
        showSlides();
    }

    // Automatic slideshow
    setInterval(nextSlide, 3000); // Change 3000 to adjust slide transition speed

