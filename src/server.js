function fetchWithRetry(url, retries, delay) {
  return new Promise((resolve, reject) => {
    const attemptFetch = (n) => {
      fetch(url)
        .then(response => {
          if (!response.ok) {
            if (n > 1) {
              setTimeout(() => {
                attemptFetch(n - 1);
              }, delay);
            } else {
              reject(new Error('Failed to fetch data: ' + response.statusText));
            }
          } else {
            return response.json()
              .then(data => resolve(data))
              .catch(jsonError => {
                reject(new Error('Failed to parse JSON: ' + jsonError.message));
              });
          }
        })
        .catch(error => {
          if (n > 1) {
            setTimeout(() => {
              attemptFetch(n - 1);
            }, delay);
          } else {
            reject(new Error('Failed to fetch data: ' + error.message));
          }
        });
    };

    attemptFetch(retries);
  });
}



